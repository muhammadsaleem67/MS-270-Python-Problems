square = lambda x: x ** 2
print(square(6)) 
"""
	1. The Keyword: We use the keyword lambda instead of def.
	2. The Argument: x acts as the parameter (like what goes inside the parentheses of a normal function).
	3. The Expression: The colon : separates the parameter from the action. x 2 is the math being performed. A lambda function automatically returns the result of this expression; you don't write the word return.
	4. Assignment: We assign this nameless function to a variable called square so we can actually call it later using square(6).
"""